package com.kuang.operator;

public class Demo03 {
    public static void main(String[] args) {
        //关系运算符返回的结果： 正确  错误  布尔值
        // 跟if配合使用

        int a = 10;
        int b = 20;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }


}
